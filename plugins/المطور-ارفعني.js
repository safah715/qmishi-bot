/* ⚡ 𝗠𝗔𝗦𝗛 𝗕𝗢𝗧 ⚡ - أمر الترقية الذكي */

let handler = async (m, { conn, isAdmin }) => {
  // إذا كان المستخدم هو البوت نفسه، لا تفعل شيئاً
  if (m.fromMe) return;

  // إذا كان المستخدم مشرفاً بالفعل
  if (isAdmin) {
    return m.reply(`*꒷꒦꒷꒷꒦꒷꒷꒦꒷꒷𝅄 ۫ ִᗀᩙᰰ ̼𝆬🌙̸〫 ᮭ࣪࣪ ⸼۫  ꒷꒦꒷꒦꒷꒷꒷꒦꒷*\n*⃝⚡┆⚠️ أنـت مـشـرف بـالـفـعـل يـا مـطـوري ❤️*\n*⃝🌙┆تـابـع عـمـلـك الـرائـع!*\n*꒷꒦꒷꒷꒦꒷꒷꒦꒷꒷𝅄 ۫ ִᗀᩙᰰ ̼𝆬🌙̸〫 ᮭ࣪࣪ ⸼۫  ꒷꒦꒷꒦꒷꒷꒷꒦꒷*`);
  }

  try {
    // محاولة رفع المستخدم لمشرف
    await conn.groupParticipantsUpdate(m.chat, [m.sender], 'promote');
    
    await m.reply(`*꒷꒦꒷꒷꒦꒷꒷꒦꒷꒷𝅄 ۫ ִᗀᩙᰰ ̼𝆬🌙̸〫 ᮭ࣪࣪ ⸼۫  ꒷꒦꒷꒦꒷꒷꒷꒦꒷*\n*⃝⚡┆✅ تـم تـرقـيـتـك إلـى مـشـرف بـنـجـاح*\n*⃝🌙┆تـذكـر: الـسـلـطـة تـعـني الـمـسؤولـية!*\n*꒷꒦꒷꒷꒦꒷꒷꒦꒷꒷𝅄 ۫ ִᗀᩙᰰ ̼𝆬🌙̸〫 ᮭ࣪࣪ ⸼۫  ꒷꒦꒷꒦꒷꒷꒷꒦꒷*\n\n ۫ ִᗀᩙᰰ ̼𝆬🌙̸〫 ᮭ࣪࣪ ⸼۫  𝗠𝗔𝗦𝗛 𝗕𝗢𝗧 𝅄 ۫ ִᗀᩙᰰ ̼𝆬🌙`);
  } catch (e) {
    // في حال فشل البوت (غالباً لأنه ليس مشرفاً)
    await m.reply(`*⃝⚡┆⚠️ عـذراً، لا أمـلـك الـصـلاحـيـات الـكـافـيـة لـتـرقـيـتـك*`);
  }
};

handler.command = /^رولي|ارفعني$/i;
handler.botAdmin = true; // البوت يجب أن يكون مشرفاً
handler.group = true;    // الأمر يعمل في المجموعات فقط
handler.owner = true;    // جعلته للمطور فقط بناءً على منطق الكود الأصلي

export default handler;
