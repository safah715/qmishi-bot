let handler = m => m;

handler.all = async function (m) {
    // *𝐦𝐚𝐬𝐡 𝐛𝐨𝐭* ⏤͟͟͞⚡: تم حذف أي شروط تمنع البوت من الرد على نفسه أو على الرسائل الصادرة من الحساب المرتبط
    
    // التحقق من وجود نص في الرسالة
    if (!m.text) return !0;

    let responses;
    // تنظيف النص وجعله متوافقاً مع البحث
    let text = m.text.toLowerCase().trim();

    // تست
    if (/^تست$/i.test(text)) {
        responses = [
            '*⌟ مـــاش شـــغــال يــســطا ⚡🔥 ⌜*',
            '*⌟ عـــايــز ايــه مــنــي 💪⚡ ⌜*',
            '*⌟ شـــغــال والـــدنــيــا فــل يـا صــاحــب  ✨👊 ⌜*',
        ];

    // بوت
    } else if (/^بوت$/i.test(text)) {
        responses = [
            '*⌟ مـــاش مـــعــاڪ 👊🔥 ⌜*',
            '*⌟ لــــســـه شــــغــال ⚡💪 ⌜*',
            '*⌟ شـــــايـــفــــني بـــوت 😎👊 ⌜*',
        ];

    // ماش
    } else if (/^ماش$/i.test(text)) {
        responses = [
            '*⌟ قـــــاـــــب مـــاش ⚡🔥 ⌜*',
            '*⌟ مـــاش عــــــلــــى قــيــد الـــحــيـــاه 💪⚡ ⌜*',
            '*⌟ نــعـــم يــا غــالــي 👊✨ ⌜*',
            '*⌟ ايـــــــه يـــــا بــاشــا ⚡👑 ⌜*'
        ];

    // المطور
    } else if (/^رياض$/i.test(text)) {
        responses = [
            '*⌟ اســـحــــب ڪــلامــك ده مـــطـــوري 👑⚡ ⌜*',
            '*⌟ قـــلـــب مـــطــــوࢪي 💪🔥 ⌜*',
            '*⌟ قــــلــــبـــــو 👊✨ ⌜*',
        ];
    }

    // هاي
    else if (/^هاي$/i.test(text)) {
        responses = [
            '*⌟ هـــاي عـــامــل ايــه ⚡🔥*',
            '*⌟ ڪـــيفــك يـا حـــب 👊😎 ⌜*',
            '*⌟ هــــاي شــو اخـــبــاࢪڪ ⚡✨ ⌜*',
        ];
    }

    // سلام
    else if (/^سلام$/i.test(text)) {
        responses = [
            '*⌟ وعـــلـــيـــڪــم الــســـلام 🌙 ⌜*',
            '*⌟ وٍعَلْيّكَمُ ٱلْسَلآمّ وٍرٍحَمُةٌ اللَّــْـْہ ۆبُركَاتة 🌓 ⌜*',
            '*⌟ ســــلام يـــغـــالي 🍸 ⌜*'
        ];
    }

    // بحبك
    else if (/^بحبك$/i.test(text)) {
        responses = [
            '*⌟ مـــاش ڪـــمــان بــيــحــبــك ❤️‍🔥⚡ ⌜*',
            '*⌟ وانــا كـــمـــان 💪❤️ ⌜*',
            '*⌟ يـــا قــلــبــي 🥺💗 ⌜*'
        ];
    }

    // إرسال الرد
    if (responses) {
        let randomIndex = Math.floor(Math.random() * responses.length);
        // تم التعديل بواسطة *𝐦𝐚𝐬𝐡 𝐛𝐨𝐭* ⏤͟͟͞⚡ لضمان الرد في كل الحالات
        await this.reply(m.chat, responses[randomIndex], m);
    }

    return !0;
};

export default handler;